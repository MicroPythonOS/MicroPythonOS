def _get(out, idx):
    off = idx * 2
    if off + 1 >= len(out):
        return None
    raw = out[off] | (out[off + 1] << 8)
    if raw >= 0x8000:
        raw -= 0x10000
    return raw


class TestADPCM(unittest.TestCase):

    def test_samples_per_block_mono(self):
        self.assertEqual(samples_per_block(1024, 1), 2041)

    def test_samples_per_block_stereo(self):
        self.assertEqual(samples_per_block(1024, 2), 1017)

    def test_samples_per_block_various(self):
        self.assertEqual(samples_per_block(256, 1), 505)
        self.assertEqual(samples_per_block(512, 2), 505)

    def test_decode_mono_block(self):
        out = decode_block(MONO_BLOCK, 1, MONO_BA)
        self.assertEqual(len(out), MONO_SPB * 1 * 2)
        for idx, expected in MONO_EXPECTED:
            actual = _get(out, idx)
            self.assertEqual(actual, expected, "sample %d: expected %d, got %d" % (idx, expected, actual))

    def test_decode_mono_checksum(self):
        out = decode_block(MONO_BLOCK, 1, MONO_BA)
        total = 0
        for i in range(0, len(out), 2):
            total += abs(_get(out, i // 2))
        self.assertEqual(total, MONO_CHECKSUM)

    def test_decode_stereo_block(self):
        out = decode_block(STEREO_BLOCK, 2, STEREO_BA)
        self.assertEqual(len(out), STEREO_SPB * 2 * 2)
        for idx, l_exp, r_exp in STEREO_EXPECTED:
            l_act = _get(out, idx * 2)
            r_act = _get(out, idx * 2 + 1)
            self.assertEqual(l_act, l_exp,
                             "stereo L[%d]: expected %d, got %d" % (idx, l_exp, l_act))
            self.assertEqual(r_act, r_exp,
                             "stereo R[%d]: expected %d, got %d" % (idx, r_exp, r_act))

    def test_decode_stereo_checksum(self):
        out = decode_block(STEREO_BLOCK, 2, STEREO_BA)
        total = 0
        for i in range(0, len(out), 4):
            total += abs(_get(out, i // 2))
            total += abs(_get(out, i // 2 + 1))
        self.assertEqual(total, STEREO_CHECKSUM * 2)

    def test_decode_identity_mono_header_zero(self):
        block = bytearray(4)
        block[0] = 0
        block[1] = 0
        out = decode_block(bytes(block), 1, 4)
        self.assertEqual(len(out), 2, "only header sample decoded")
        self.assertEqual(_get(out, 0), 0)

    def test_decode_identity_stereo_silent_block(self):
        block = bytearray(8)
        for ch in range(2):
            block[ch * 4] = 0
            block[ch * 4 + 1] = 0
            block[ch * 4 + 2] = 0
        out = decode_block(bytes(block), 2, 8)
        self.assertEqual(len(out), 1 * 2 * 2, "only header samples with ba=8")
        self.assertEqual(_get(out, 0), 0)
        self.assertEqual(_get(out, 1), 0)

    def test_decode_truncated_block_raises(self):
        with self.assertRaises(IndexError):
            decode_block(bytes(2), 1, 4)

    def test_decode_negative_predictor_clip(self):
        block = bytearray(4)
        block[0] = 0
        block[1] = 0x80
        out = decode_block(bytes(block), 1, 4)
        self.assertEqual(_get(out, 0), -32768)

    def test_decode_positive_predictor_clip(self):
        block = bytearray(4)
        block[0] = 0xFF
        block[1] = 0x7F
        out = decode_block(bytes(block), 1, 4)
        self.assertEqual(_get(out, 0), 32767)
